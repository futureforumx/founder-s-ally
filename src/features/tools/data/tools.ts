import type { Tool, ToolAutonomy, ToolCategory, ToolSkillLevel } from "@/features/tools/types";

type Seed = {
  name: string;
  slug: string;
  subcategory: string;
  type: string;
  shortDescription: string;
  description: string;
  websiteUrl?: string | null;
  pricing?: string;
  freeTier?: boolean | null;
  openSource?: boolean | null;
  skillLevel?: ToolSkillLevel;
  autonomy?: ToolAutonomy;
  mobileApp?: boolean | null;
  bestFor?: string[];
  useCases?: string[];
  pros?: string[];
  cons?: string[];
  alternatives?: string[];
  tags?: string[];
  featured?: boolean;
  trending?: boolean;
};

const defaultBestFor = ["Founders comparing new software", "Teams evaluating tools by workflow"];
const defaultUseCases = ["Researching tools", "Shortlisting options"];
const defaultPros = ["Clear category fit", "Useful as a starting point for evaluation"];
const defaultCons = ["Not every vendor publishes full public details", "Feature depth can change over time"];

function buildTool(category: ToolCategory, seed: Seed): Tool {
  return {
    name: seed.name,
    slug: seed.slug,
    category,
    subcategory: seed.subcategory,
    type: seed.type,
    shortDescription: seed.shortDescription,
    description: seed.description,
    websiteUrl: seed.websiteUrl ?? null,
    pricing: seed.pricing ?? "Pricing not listed",
    freeTier: seed.freeTier ?? null,
    openSource: seed.openSource ?? null,
    skillLevel: seed.skillLevel ?? "Unknown",
    autonomy: seed.autonomy ?? "N/A",
    popularity: null,
    userRating: null,
    mobileApp: seed.mobileApp ?? null,
    bestFor: seed.bestFor ?? defaultBestFor,
    useCases: seed.useCases ?? defaultUseCases,
    pros: seed.pros ?? defaultPros,
    cons: seed.cons ?? defaultCons,
    alternatives: seed.alternatives ?? [],
    tags: seed.tags ?? [],
    featured: seed.featured ?? false,
    trending: seed.trending ?? false,
  };
}

